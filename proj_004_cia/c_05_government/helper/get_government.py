    # //////////////////////////////////////////////////////////////////////////////////////////////////
    # NOTE: 4 >>> 'Constitution'
    # --------------------------------------------------------------------------------------------------
    constitution_data = gov_data.get("Constitution", {})
    # --------------------------------------------------------------------------------------------------
