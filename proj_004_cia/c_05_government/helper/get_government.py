    # //////////////////////////////////////////////////////////////////////////////////////////////////
    # NOTE: 27 >>> 'Suffrage'
    # --------------------------------------------------------------------------------------------------
    suffrage_data = gov_data.get("Suffrage", {})
    # --------------------------------------------------------------------------------------------------

    if info == 'suffrage':
        return parse_suffrage(iso3Code)

    # 28
    # //////////////////////////////////////////////////////////////////////////////////////////////////
    # NOTE: 28 >>> 'Union name'
    # --------------------------------------------------------------------------------------------------
    union_name_data = gov_data.get("Union name", {})
    # --------------------------------------------------------------------------------------------------

