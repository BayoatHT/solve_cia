    # 1
    # //////////////////////////////////////////////////////////////////////////////////////////////////
    # NOTE: 1 >>> 'Administrative divisions'
    # --------------------------------------------------------------------------------------------------
    admin_divisions_data = gov_data.get("Administrative divisions", {})
    # --------------------------------------------------------------------------------------------------

