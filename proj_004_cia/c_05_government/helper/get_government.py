def get_government(data=None, info=None, iso3Code=None):

    # 0
    # //////////////////////////////////////////////////////////////////////////////////////////////////
    # NOTE: GOVERNMENT DATA
    # --------------------------------------------------------------------------------------------------
    gov_data = data.get("Government", {})
