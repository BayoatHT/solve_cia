def parse_union_name(
    test_data: dict,
    iso3Code: str = None
, return_original: bool = False)-> dict:
    """Parse union name from CIA Government section."""
    if return_original:
        return test_data

    result = {}
    if not test_data or not isinstance(test_data, dict):
        return result
    try:
        field_mappings = {
            'conventional long form': 'union_long_form',
            'abbreviation': 'union_abbreviation',
        }
        for cia_key, output_key in field_mappings.items():
            if cia_key in test_data:
                field_data = test_data[cia_key]
                if isinstance(field_data, dict) and 'text' in field_data:
                    text = field_data['text']
                    if text and isinstance(text, str):
                        result[output_key] = clean_text(text)
                elif isinstance(field_data, str) and field_data.strip():
                    result[output_key] = clean_text(field_data)
        if 'note' in test_data:
            note = test_data['note']
            if isinstance(note, str) and note.strip():
                result['union_name_note'] = clean_text(note)
    except Exception as e:
        app_logger.error(f"Error parsing union_name: {e}")
    return result


